//Print Inverted-Star Pattern -2

package Patterns;
public class InvertedStar {
    public static void main(String[] args) {
        for (int i = 0; i <= 5; i++) {
            for (int j = i; j <= 5; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}

